// In JavaScript 'while' loop is a control flow statement that allows you to repeatedly execute a block of code as long as a specified condition evaluate to 'true'.

// Syntax
// while (condition) {
//   // Code to be execute as long as condition is true
// }

// example
let i = 0;

while (i < 5) {
  console.log("rakib");
  i++;
}

// display 1 to 10
let count = 1;

while (count <= 10) {
  console.log(count);
  count++;
}

// display even number up to 20
let evenNum = 2;

while (evenNum <= 20) {
  console.log(evenNum);
  evenNum += 2;
}

// decrementing from 10 to 1
let countdown = 10;

while (countdown >= 1) {
  console.log(countdown);
  countdown--;
}
